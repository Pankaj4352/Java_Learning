package Operators;

public class EX_01_Concatenation {
    public static void main(String []args)
    {
        // + operators
        int a=10 , b = 20;
        System.out.println(a+b);

        // + operators with string act as concatenation.
        String fname = "Rahul";
        String lname = "Yadav";
        System.out.println(fname+lname);
        /*
        '-' , '*', '/' , '%' operators did not work with string.
        System.out.println(fname-lname);
        System.out.println(fname*lname);
        System.out.println(fname/lname);
        System.out.println(fname%lname);

         */

    }
}
